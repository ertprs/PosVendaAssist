				No dia 25 de maio comemora-se mundialmente o <b>Dia da Toalha</b>.
				<br>
				Uma homenagem ao saudoso escritor <b>Douglas Adams</b>, autor da série de livros "<b>Guia do Mochileiro das Galáxias</b>".
				<br><br>
				A série de cinco livros, escrita em forma de comédia, debocha de muitos dos temas atuais de nossa sociedade, como política, economia, guerras, burocracia, religião e ética, bem como temas do universo nerd, como viagens no tempo, computação, viagens espaciais, dentre outros tantos assuntos.
				<br><br>
				Os temas não são abordados pelo lado da ficção científica, mas exatamente pelo oposto, ou seja, o quão risível e ridículo estas situações podem se mostrar.
				<br><br>
				Uma boa leitura para nerds e afins. Riso garantido para bons entendedores de piadas bem elaboradas.
				<br><br>
				<b>Em um dos trechos, são descritas quais as utilidades de uma toalha para um viajante espacial:</b>
				<br><br>
				"A toalha é um dos objetos mais úteis para um mochileiro interestelar. Em parte devido a seu valor prático: você pode usar a toalha como agasalho quando atravessar as frias luas de Beta de Jagla; pode deitar-se sobre ela nas reluzentes praias de areia marmórea de Santragino V, respirando os inebriantes vapores marítimos; você pode dormir debaixo dela sob as estrelas que brilham avermelhadas no mundo desértico de Kakrafoon; pode usá-la como vela para descer numa minijangada as águas lentas e pesadas do rio Moth; pode umedecê-la e utilizá-la para lutar em um combate corpo a corpo; enrolá-la em torno da cabeça para proteger-se de emanações tóxicas ou para evitar o olhar da Terrível Besta Voraz de Traal (um animal estonteantemente burro, que acha que, se você não pode vê-lo, ele também não pode ver você - estúpido feito uma anta, mas muito, muito voraz); você pode agitar a toalha em situações de emergência para pedir socorro; e naturalmente pode usá-la para enxugar-se com ela se ainda estiver razoavelmente limpa".
				<br><br>
				Outra grande importância de uma toalha é cultivar bactérias que podem ser ofensivas em um certo planeta, mas muito salvadoras em outro. Então, bastaria chupar o canto certo da toalha para ter um remédio eficaz.
